package Operater;
public class AssignmentOperater {
    public static void main(String[] args){
        // these are compound assignment operater
        int a = 10;
        int b = 2;
        a += b; // this means a =a + b;
        System.out.println(a);
        a -= b; // this means a =a - b;
        System.out.println(a);
        a *= b; // this means a =a * b;
        System.out.println(a);
        a /= b; // this means a =a / b;
        System.out.println(a);
        a %= b; // this means a =a % b;
        System.out.println(a);
        
    }
}
