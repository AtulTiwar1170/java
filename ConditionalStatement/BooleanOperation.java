package ConditionalStatement;

public class BooleanOperation {
    public static void main(String[] args){
        boolean a = true;
        boolean b = true;
        boolean c = true;
        boolean d = true;
        System.out.println(c);
        System.out.println(d);
        System.out.println( !a);
        System.out.println( !b);
        System.out.println( !c);
        System.out.println( !d);
        System.out.println(a && b);
        System.out.println(a || b);
        System.out.println(!(a && b));
    }
}
